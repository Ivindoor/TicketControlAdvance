package com.sanmina.tk.orm.model;
import javax.persistence.Entity;
import javax.persistence.Table;
import javax.persistence.Id;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Column;
import lombok.Getter;
import lombok.Setter;

@Entity
@Table(name="tbl_MDS", schema="dbo", catalog="SFDC_Ticket_Control")
public class tbl_MDS {

	
@Id	
@GeneratedValue(strategy=GenerationType.AUTO)	
@Column(name="idMDS")	
@Getter @Setter private Integer idMDS;

@Column(name="MDS", nullable=true)	
@Getter @Setter private String MDS;





}
